    private void CrearCursojButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearCursojButtonActionPerformed
        // TODO add your handling code here:
        
        Curso c = new Curso();
            c.cursoNom = nombreCursojTextField.getText();
            c.seccion = seccionjTextField.getText();
            c.fechaInicio = fechaIniciojTextField.getText();
            c.fechaFin = fechaFinjTextField.getText();
            c.horaInicio = horaIniciojTextField.getText();
            c.horaFin = horaFinjTextField.getText();
            c.id = codigojTextField.getText();
            c.profAsig =(String) profesorjComboBox.getSelectedItem();
            
            Login.cursos.add(c);
            
            nombreCursojTextField.setText("");
            seccionjTextField.setText("");
            fechaIniciojTextField.setText("");
            fechaFinjTextField.setText("");
            horaIniciojTextField.setText("");
            horaFinjTextField.setText("");
            codigojTextField.setText("");
            
            JOptionPane.showMessageDialog(this, "Curso Creado!");
    }//GEN-LAST:event_CrearCursojButtonActionPerformed

    

